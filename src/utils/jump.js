export function jump(number) {
    const title = undefined;
    switch (true) {
        case number == 0:
            title = '会有中心';
            return title;
        case number == 1:
            title = '推广赚钱';
            return title;
        case number == 2:
            title = '分享';
            return title;
        case number == 3:
            title = '机器人';
            return title;
    }
}